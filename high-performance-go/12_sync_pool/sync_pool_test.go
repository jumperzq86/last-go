package _2_sync_pool

import (
	"bytes"
	"sync"
	"testing"
)

var buffPool = sync.Pool{
	New: func() interface{} {
		return &bytes.Buffer{}
	},
}

var data = make([]byte, 10000)

func BenchmarkBufferWithPool(b *testing.B) {
	for n := 0; n < b.N; n++ {
		buf := buffPool.Get().(*bytes.Buffer)
		buf.Write(data)
		buf.Reset()
		buffPool.Put(buf)
	}
}

func BenchmarkBuffer(b *testing.B) {
	for n := 0; n < b.N; n++ {
		var buf bytes.Buffer
		buf.Write(data)
	}
}
